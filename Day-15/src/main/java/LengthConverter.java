class LengthConverter {

    public double centimeterToCentimeter(double lengthInCentimeter){return lengthInCentimeter;}
    public double centimeterToMeter(double lengthInCentimeter){
        return lengthInCentimeter/100;
    }
    public double meterToCentimeter(double lengthInMeter){return lengthInMeter*100;}
    public double centimeterToKilometer(double lengthInCentimeter){
        return lengthInCentimeter/100000;
    }
    public double kilometerToCentimeter(double lengthInKilometer){return lengthInKilometer*100000;}



}
